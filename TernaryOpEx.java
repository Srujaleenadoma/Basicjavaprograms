//program using Ternary Operators
public class TernaryOpEx{
        public static void main(String args[]){
                 int num1=10;
                 int num2=30;
                 int num3=50;
                 int min=num1<num2?num1:num2;
                 System.out.println(min); 
                 int max=num2<num3?num2:num3;
                 System.out.println(max);
}
}